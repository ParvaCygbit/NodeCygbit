// OS Module
const os = require("os");
console.log("OS HostName:", os.hostname()); // this will give the hostname of the OS
console.log("OS Free Memory:", os.freemem()); // this will give the free memory of the OS
console.log("OS Total Memory:", os.totalmem()); // this will give the total memory of the OS
console.log("OS Platform:", os.platform()); // this will give the platform of the OS
console.log("OS Type:", os.type()); // this will give the type of the OS
console.log("OS Release:", os.release()); // this will give the release of the OS
console.log("OS Uptime:", os.uptime()); // this will give the uptime of the OS
